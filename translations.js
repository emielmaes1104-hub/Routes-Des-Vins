/* ── Language Engine ── */
(function() {
  let currentLang = localStorage.getItem('rdv-lang') || 'nl';

  function t(key) {
    return window.RDV.translations[currentLang][key] || window.RDV.translations['nl'][key] || key;
  }

  function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    });

    document.querySelectorAll('[data-i18n-href]').forEach(el => {
      const key = el.getAttribute('data-i18n-href');
      el.setAttribute('href', t(key));
    });

    document.documentElement.lang = currentLang;

    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === currentLang);
    });
  }

  function setLang(lang) {
    currentLang = lang;
    localStorage.setItem('rdv-lang', lang);
    applyTranslations();
  }

  window.RDV.t = t;
  window.RDV.setLang = setLang;
  window.RDV.getLang = () => currentLang;

  document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => setLang(btn.dataset.lang));
    });
  });
})();

/* ── Scroll Reveal ── */
(function() {
  document.addEventListener('DOMContentLoaded', () => {
    const observer = new IntersectionObserver((entries) => {
      entries.forEach(e => {
        if (e.isIntersecting) {
          e.target.classList.add('visible');
          observer.unobserve(e.target);
        }
      });
    }, { threshold: 0.1, rootMargin: '0px 0px -40px 0px' });

    document.querySelectorAll('.reveal, .reveal-left, .reveal-scale').forEach(el => {
      observer.observe(el);
    });
  });
})();

