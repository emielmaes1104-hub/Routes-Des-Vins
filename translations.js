/* ── Countdown ── */
(function() {
  document.addEventListener('DOMContentLoaded', () => {
    const target = new Date('2026-11-20T15:30:00');
    const els = {
      d: document.getElementById('cd-days'),
      h: document.getElementById('cd-hours'),
      m: document.getElementById('cd-mins'),
      s: document.getElementById('cd-secs')
    };

    if (!els.d) return;

    function pad(n) { return String(n).padStart(2, '0'); }

    function tick() {
      const diff = target - Date.now();
      if (diff <= 0) {
        Object.values(els).forEach(el => { if (el) el.textContent = '00'; });
        return;
      }
      const days  = Math.floor(diff / 86400000);
      const hours = Math.floor((diff % 86400000) / 3600000);
      const mins  = Math.floor((diff % 3600000) / 60000);
      const secs  = Math.floor((diff % 60000) / 1000);
      if (els.d) els.d.textContent = pad(days);
      if (els.h) els.h.textContent = pad(hours);
      if (els.m) els.m.textContent = pad(mins);
      if (els.s) els.s.textContent = pad(secs);
    }

    tick();
    setInterval(tick, 1000);
  });
})();

/* ── FAQ Accordion ── */
(function() {
  document.addEventListener('DOMContentLoaded', () => {
    document.querySelectorAll('.faq-trigger').forEach(trigger => {
      trigger.addEventListener('click', () => {
        const expanded = trigger.getAttribute('aria-expanded') === 'true';
        // Close others (skip current trigger)
        document.querySelectorAll('.faq-trigger').forEach(t => {
          if (t === trigger) return;
          t.setAttribute('aria-expanded', 'false');
          const body = t.nextElementSibling;
          if (body) body.classList.remove('open');
        });
        // Toggle this
        const nowOpen = !expanded;
        trigger.setAttribute('aria-expanded', String(nowOpen));
        const body = trigger.nextElementSibling;
        if (body) body.classList.toggle('open', nowOpen);
      });
    });
  });
})();
